#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*new_list;

	new_list = (t_list *)malloc(sizeof(t_list));
	if (new_list != 0)
	{
		new_list->content = content;
		new_list->next = NULL;
	}
	return (new_list);
}
